import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

import { format, resolveConfig } from "prettier";

import { readLatestValidatedEvidence } from "../tools/local-sandbox/evidence";

const projectRoot = resolve(import.meta.dirname, "..");
const outputPath = resolve(projectRoot, "src/demo/observability/generated.ts");
const summaryOutputPath = resolve(
  projectRoot,
  "src/demo/observability/recordedSummary.generated.ts",
);
const check = process.argv.includes("--check");
const prettierConfig = (await resolveConfig(resolve(projectRoot, "package.json"))) ?? {};

async function formatSource(source: string): Promise<string> {
  return format(source, { ...prettierConfig, parser: "typescript" });
}

async function generatedSources(): Promise<{
  readonly trace: string;
  readonly summary: string;
}> {
  const latest = await readLatestValidatedEvidence(projectRoot);
  if (!latest?.trace || latest.pack.schemaVersion !== 3) {
    return {
      trace: await formatSource(
        `/* Generated by scripts/generate-recorded-trace.ts. Do not edit. */\nimport type { RecordedRunTrace } from "./contracts";\n\nexport const recordedRunTrace: RecordedRunTrace | null = null;\n`,
      ),
      summary: await formatSource(
        `/* Generated by scripts/generate-recorded-trace.ts. Do not edit. */\nimport type { RecordedWalkthroughEvidence } from "./contracts";\n\nexport const recordedWalkthroughEvidence: RecordedWalkthroughEvidence | null = null;\n`,
      ),
    };
  }
  const payload = {
    trace: latest.trace,
    evidence: {
      runId: latest.pack.run.id,
      status: latest.pack.run.status,
      sourceCommit: latest.pack.run.sourceCommit,
      sourceWorkingTree: latest.pack.run.sourceWorkingTree,
      testedRepositoryTreeDigest: latest.trace.bindings.testedRepositoryTreeDigest,
      evidenceDigest: latest.pack.evidenceDigest,
      traceArtifact: latest.pack.observability.trace.artifact,
      traceArtifactSha256: latest.pack.observability.trace.artifactSha256,
      evidenceArtifact: latest.index.latest.json,
      evidenceMarkdownArtifact: latest.index.latest.markdown,
      sandboxProvider: latest.pack.boundary.limits.provider.kind,
      change: {
        path: latest.pack.change.path,
        expectedTextSha256: latest.pack.change.expectedTextSha256,
        replacementTextSha256: latest.pack.change.replacementTextSha256,
        changedFiles: latest.pack.change.changedFiles.map((file) => file.path),
        unifiedDiff: latest.pack.change.unifiedDiff,
        unifiedDiffSha256: latest.pack.change.unifiedDiffSha256,
      },
      budget: latest.pack.observability.budget,
      accounting: latest.pack.observability.accounting,
      approval: latest.pack.observability.approval,
    },
  };
  const summaryPayload = {
    runId: latest.pack.run.id,
    sourceCommit: latest.pack.run.sourceCommit,
    sourceWorkingTree: latest.pack.run.sourceWorkingTree,
    sandboxProvider: latest.pack.boundary.limits.provider.kind,
    approvalOutcome: latest.pack.observability.approval.outcome,
    change: {
      path: latest.pack.change.path,
      expectedTextSha256: latest.pack.change.expectedTextSha256,
      replacementTextSha256: latest.pack.change.replacementTextSha256,
      unifiedDiff: latest.pack.change.unifiedDiff,
      unifiedDiffSha256: latest.pack.change.unifiedDiffSha256,
    },
  };
  return {
    trace: await formatSource(
      `/* Generated by scripts/generate-recorded-trace.ts. Do not edit. */\nimport type { RecordedRunTrace } from "./contracts";\n\nexport const recordedRunTrace: RecordedRunTrace | null = ${JSON.stringify(payload, null, 2)};\n`,
    ),
    summary: await formatSource(
      `/* Generated by scripts/generate-recorded-trace.ts. Do not edit. */\nimport type { RecordedWalkthroughEvidence } from "./contracts";\n\nexport const recordedWalkthroughEvidence: RecordedWalkthroughEvidence | null = ${JSON.stringify(summaryPayload, null, 2)};\n`,
    ),
  };
}

const expected = await generatedSources();
if (check) {
  const current = await readFile(outputPath, "utf8").catch(() => "");
  const currentSummary = await readFile(summaryOutputPath, "utf8").catch(() => "");
  if (current !== expected.trace || currentSummary !== expected.summary) {
    process.stderr.write("Recorded trace module is stale. Run npm run trace:generate.\n");
    process.exitCode = 1;
  }
} else {
  await writeFile(outputPath, expected.trace, "utf8");
  await writeFile(summaryOutputPath, expected.summary, "utf8");
}
